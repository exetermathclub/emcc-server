<h3>I am interested in applying to Phillips Exeter Academy. If I or my team 
wins the competition, will I be admitted to Exeter?</h3>

<p>No. It's great that you'd like to come to Exeter, and we encourage you to
put in your application your partcipation/good results. However, doing well
at this competition may not help you get accepted, or even get waitlisted,
into Exeter any more than doing well or winning any other math competition
will. If you have any questions regarding admission to PEA, you can learn more
at the <a href="http://exeter.edu/admissions/default.aspx">Admissions Website.</a></p>

