<h1>FAQ</h1>

<h2>General</h2>

<h3>How do I get to Exeter?</h3>

<p>You can find driving directions as well as a copy of Exeter's campus map 
<a href="http://www.exeter.edu/news_and_events/news_events_2105.aspx#">here</a>.
The competition will be held in the Academy Center on Tan Lane.</p>

<p> We will also provide some buses to/from the airport and hotels 
for those who are flying in. 
Please see <a href="http://csserver.exeter.edu/~mathclub/emc2/information.php#transportation">here</a> for more details and pickup times.
</p>


<h3>What happens if I arrive late?</h3>

<p>If necessary, we will push back the start time of the competition in order to 
account for bad weather. Otherwise, you will need to wait for the start of the 
next round in order to join the competition.</p>

<h3>Is lunch included?</h3>

<p>A pizza lunch for students, coaches, and proctors is included with the price of registration.</p>

<h3>How many coaches and/or parents can we bring?</h3>

<p>We welcome all coaches and parents who would like to come, and they will be able to watch their students compete during the guts round. In addition, we will provide a setting for parents and coaches to wait and mingle while students are taking the earlier rounds. </p>

<h2>Contests</h2>

<h3>What topics will be included in the contest?</h3>

<p>The contest will not include topics that are not taught in middle school. General areas that will be covered include counting and probability, geometry, and basic algebra.</p>

<h3>How hard will the contest be?</h3>

<p>The contest will include problems approachable to students of all skill 
levels, from the young novice fresh out of elementary school to the
veteran 8th grade student already competing at the high-school level.
While all the problems can be solved using middle-school level mathematics,
there will certainly be problems difficult enough that we expect only the top
competitors to solve them.</p>

<h3>Are calculators allowed?</h3>

<p>No; calculators may not be used for any portion of the test.</p>

<h3>I'm registered as an individual. Can I still take the team and guts rounds?</h3>

<p>Yes; we will create teams out of individual entries.</p>

