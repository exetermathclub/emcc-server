<h2>About EMC2</h2>

<p>The Exeter Math Club Competition (EMC2) is a middle-school math competition
held at Phillips Exeter Academy in Exeter, New Hampshire. The 4th annual EMC2
is scheduled for Saturday, January 26, 2013.</p>
 
<p>This is an excellent opportunity for young aspiring mathematicians to
develop their mathematical talents, get tournament experience, bond with
their friends, and connect with other students from different schools. The
tournament is being run mostly by Exeter Math Club students, but with some
help from our teachers. Most of us have participated in math contests in
middle school and all of us participate in high school contests, and
we&#39;ve tried to incorporate some of our favorite parts of those
competitions into EMC2.</p>

<p>The 2012 tournament consisted of two individual rounds, a team round, and a
&quot;guts&quot; round. The guts round is a fun and exciting addition to regular written rounds. It is a fast-paced, hour-long team round where 24 problems are given in
sets of three, one set at a time for the team to solve together.Teams may spend as much time as they like on each set. Grading is in real time and as
soon as you turn in one set, you receive the next immediately. We&#39;ll be
looking to modify and improve the test format this coming year but these events
will probably form the core of the competition.</p>

<p>Teams should consist of 4 students in the 8th grade or below, with one coach
or other chaperone per team. Teams representing a single school should not have
students from outside that school. Each school may now enter as many teams as
they wish. Individual students or aggregate teams can also enter, and
we will sort them into teams of four ourselves. The cost for a full team
is $50, and $15 for individual entries.</p>

<p>Two years ago, we opened up the tournament to traveling teams from outside the local region; 
as a result, last year we had over 180 students, with teams from far-flung places including China and Illinois 
and the numbers keep increasing. This year too, we&#39;re looking to keep the competition open for any teams willing to travel. 
Teams would be able to leave for home Saturday afternoon by 4:00 (we have finished on or ahead of schedule each of the last two years), 
and we will work to provide transportation to the airport if needed.</p>

