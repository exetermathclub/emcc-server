<h2><a name="format"></a>Competition Format</h2>

<p>These are the 4 rounds for the 2013 competition. We will be looking to
improve the format this coming year but these events will probably still
form the core of the competition.</p>

<h3>Individual Rounds</h3>

<p>The individual round consists of two parts, taken one after another. The first
part has 20 questions in 20 minutes, emphasizing speed. The second part is
10 questions over 30 minutes, emphasizing accuracy in problem solving. Questions
in the first part are worth 3 points each, which increases to 9 points each in
in the second round. Each student can thus earn up to 150 points, which determines
their individual ranking, and is added to the student&lsquo;s team score (so a team can
earn up to 600 points from the individual round).</p>

<h3>Team Round</h3>

<p>The team round is a 45 minute round where everybody on the team
can work together to solve 10 questions. The questions will
mostly be unrelated to each other, but some of them may be strung together by a 
common theme. Each question is worth 30 points, so a maximum of 300 points can
be earned for the team score in this round.</p>

<h3>Guts Round</h3>

<p>The Guts Round is a live round consisting of 24 questions 75 minutes. 
The questions are given to the teams in sets of three. At the beginning of the round, 
each team will send a runner down to one of the scoring tables (spaced evenly throughout 
the contest hall to minimize differences in distance) to get the first set of three problems, 
and bring it back to their team. The team then works together for as long as they need (within the 75 minutes) 
to solve these three problems. When they&lsquo;re ready, the runner then drops off their answers at a scoring table, 
where they will receive the next set of three. At each scoring table, the problems will be scored as soon as 
they are dropped off, and a running tally of each team&lsquo;s points and progress will displayed on a projector 
screen at the front of the hall. The point value for each problem increases between each set of three, 
starting at 6 points per problem in the first set, and ending at 18 points per problem in the last (8th) 
set, for a total of 300 possible points. The point value for the ten problem sets are as follows 
(6, 8, 10, 12, 14, 16, 16, 18).
</p>

<h3>Puzzle Round</h3>
<p>The Puzzle Round is an unofficial 30 minute cooldown round between Guts Round and the awards ceremony. 
    Team members will work together to solve a math-flavored logic puzzle, 
    and the fastest teams will receive a small prize. 
    Details specific to each year's puzzle round will be announced at the competition. 
    The 2013 EMCC puzzle round will be prepared by World Puzzle Champion Palmer Mebane.
</p>

<h2><a name="rules"></a>Rules</h2>

<h3>Calculators etc.</h3>
<p>Books, notes, calculators, pocket organizers, slide-rules, abaci, 
calculator wrist watches, or any other kind of computational aid 
are prohibited during all parts of the competition. <br>
The same goes for graph paper, rulers, protractors, compasses, or any other 
drawing aid. <br>
Similarly, laptops, PDAs, cell phones, or any other electronic communication 
devices are also not allowed.</p>


<h3>Answer forms:</h3>
<ul>
  <li>
	Answers need to be simplified and exact unless otherwise specified. 
	(So 3.14 will not work for pi, nor will 4/12 instead of 1/3.)
  </li> <li>
	No partial credit will be given unless specified otherwise.
  </li> <li>
	Fractions should be simplified and improper. (13/4, not 26/8 or 
	3 1/4)
  </li> <li>
	Radicals should be simplified so that the radicand does not contain
	any fractions nor be divisible by the square of any integer greater
	than one. Denominators should be rationalized.
  </li> <li>
	Correct mathematical notation should be used.
  </li>
</ul>

<p style="font-weight:bold">All decisions made by the EMC2 judges are final.</p>
 
